# Exercício 3.5  Calcule o  resultado  da expressão  A  >  B and C or D, utilizando os  valores  da  tabela  a seguir.
# --------------------------------------------------------
# |   A   |   B   |  C   |   D   |Resultado|
# |   1   |   2   |True  |False  |False    |
# |  10   |   3   |False |False  |False    |
# |   5   |   1   |True  |True   |True     |
# ---------------------------------------------------------

print(1 > 2 and True or False)
print(10 > 3 and False or False)
print(5 > 1 and True or True)
